#PRACTCA TIPOS DE DATOS

#string

texto = "Alex"
print(type(texto))
print(texto)

#numeros

num1 = 1
print(type(num1))
print(num1)
num2 = 1.1
print(type(num2))
print(num2)

#lista

lista = [1,2,"hola","mundo"]
print(type(lista))
print(lista)

#set
tipo_set = {1,2,"tipo","set"}
print(type(tipo_set))
print(tipo_set)

#diccionario
diccionario = {"clave":"valor","nombre":"alex"}
print(type(diccionario))
print(diccionario)

#tupla
tupla = (1,2,"hola","tupla")
print(type(tupla))
print(tupla)